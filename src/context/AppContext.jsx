export const AppProvider = ({ children }) => {
  const [activePage, setActivePage] = useState('landing'); // 'landing', 'shop', 'checkout', 'portal', 'admin'
  const [activeMobileTab, setActiveMobileTab] = useState('home'); // 'home', 'catalog', 'portal', 'admin'
  
  const [cart, setCart] = useState(() => {
    const saved = localStorage.getItem('cumbum_cart');
    return saved ? JSON.parse(saved) : [];
  });
  
  const [orders, setOrders] = useState(() => {
    const saved = localStorage.getItem('cumbum_orders');
    return saved ? JSON.parse(saved) : INITIAL_ORDERS;
  });
  
  const [isCartOpen, setIsCartOpen] = useState(false);
  const [isDetailsOpen, setIsDetailsOpen] = useState(false);
  const [activeProduct, setActiveProduct] = useState(null);
  const [lastCompletedOrder, setLastCompletedOrder] = useState(null);
  const [isCategoryDrawerOpen, setIsCategoryDrawerOpen] = useState(false);
  const [selectedCategory, setSelectedCategory] = useState('All');
  const [selectedSubCategory, setSelectedSubCategory] = useState('All');

  // Sync to local storage
  useEffect(() => {
    localStorage.setItem('cumbum_cart', JSON.stringify(cart));
  }, [cart]);

  useEffect(() => {
    localStorage.setItem('cumbum_orders', JSON.stringify(orders));
  }, [orders]);

  // Sync mobile tabs
  useEffect(() => {
    const tabMap = {
      'home': 'landing',
      'catalog': 'shop',
      'story': 'story',
      'portal': 'portal',
      'admin': 'admin'
    };
    setActivePage(tabMap[activeMobileTab]);
  }, [activeMobileTab]);

  const addToCart = (product, quantity = 1, size = 100) => {
    triggerHaptic(20);
    setCart((prev) => {
      const existingIdx = prev.findIndex(
        (item) => item.product.id === product.id && item.size === size
      );

      if (existingIdx > -1) {
        const nextCart = [...prev];
        const nextQty = nextCart[existingIdx].quantity + quantity;
        nextCart[existingIdx].quantity = nextQty;
        nextCart[existingIdx].price = calculateCartItemPrice(product, size, nextQty);
        return nextCart;
      }

      const itemPrice = calculateCartItemPrice(product, size, quantity);
      const cartItemId = `${product.id}-${size}-${Date.now()}`;
      return [...prev, { id: cartItemId, product, quantity, size, price: itemPrice }];
    });
  };

  const removeFromCart = (cartItemId) => {
    triggerHaptic(15);
    setCart((prev) => prev.filter((item) => item.id !== cartItemId));
  };

  const updateCartQuantity = (cartItemId, newQty) => {
    if (newQty <= 0) {
      removeFromCart(cartItemId);
      return;
    }
    triggerHaptic(10);
    setCart((prev) =>
      prev.map((item) =>
        item.id === cartItemId
          ? {
              ...item,
              quantity: newQty,
              price: calculateCartItemPrice(item.product, item.size, newQty)
            }
          : item
      )
    );
  };

  const clearCart = () => {
    setCart([]);
  };

  const submitCheckout = (shippingDetails) => {
    triggerHaptic(30);
    const newId = `ORD-${Math.floor(10000 + Math.random() * 90000)}`;
    const subtotal = cart.reduce((sum, item) => sum + item.price * item.quantity, 0);
    const newOrder = {
      id: newId,
      items: [...cart],
      total: subtotal,
      shippingDetails,
      status: 'Logged',
      createdAt: new Date().toISOString(),
      accountExecutive: 'Siddharth R.',
      trackingNotes: 'Secure payment cleared. Packaging lines packaging your heritage millet grains.'
    };

    setOrders((prev) => [newOrder, ...prev]);
    setLastCompletedOrder(newOrder);
    clearCart();
    return newOrder;
  };

  const updateOrderStatus = (id, newStatus, extraData = {}) => {
    setOrders((prev) =>
      prev.map((ord) =>
        ord.id === id
          ? {
              ...ord,
              status: newStatus,
              ...extraData,
            }
          : ord
      )
    );
  };

  const deleteOrder = (id) => {
    setOrders((prev) => prev.filter((ord) => ord.id !== id));
  };

  const triggerHaptic = (ms = 15) => {
    if (typeof window !== 'undefined' && window.navigator && window.navigator.vibrate) {
      window.navigator.vibrate(ms);
    }
  };

  return (
    <AppContext.Provider
      value={{
        activePage,
        setActivePage,
        activeMobileTab,
        setActiveMobileTab,
        products: overrideProducts,
        cart,
        orders,
        addToCart,
        removeFromCart,
        updateCartQuantity,
        clearCart,
        submitCheckout,
        updateOrderStatus,
        deleteOrder,
        isCartOpen,
        setIsCartOpen,
        isDetailsOpen,
        setIsDetailsOpen,
        activeProduct,
        setActiveProduct,
        lastCompletedOrder,
        setLastCompletedOrder,
        triggerHaptic,
        isCategoryDrawerOpen,
        setIsCategoryDrawerOpen,
        selectedCategory,
        setSelectedCategory,
        selectedSubCategory,
        setSelectedSubCategory
      }}
    >
      {children}
    </AppContext.Provider>
  );
};

export const useApp = () => {
  const context = useContext(AppContext);
  if (!context) {
    throw new Error('useApp must be used within an AppProvider');
  }
  return context;
};
