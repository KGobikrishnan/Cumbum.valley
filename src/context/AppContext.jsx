export const calculateTieredPricePerKg = (basePricePerKg, totalWeight) => {
  if (totalWeight < 100) {
    return Math.round(basePricePerKg * 1.25); // +25% markup for small orders (under 100kg)
  } else if (totalWeight >= 100 && totalWeight < 500) {
    return basePricePerKg; // Actual B2B base rate
  } else if (totalWeight >= 500 && totalWeight < 1000) {
    return Math.round(basePricePerKg * 0.95); // 5% bulk discount (500kg+)
  } else {
    return Math.round(basePricePerKg * 0.90); // 10% super exporter discount (1000kg+)
  }
};

export const calculateCartItemPrice = (product, size, quantity) => {
  const totalWeight = size * quantity;
  const ratePerKg = calculateTieredPricePerKg(product.pricePerKg, totalWeight);
  return Math.round(ratePerKg * size);
};

